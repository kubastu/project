package com.group2.project.calendarobjects;

/**
 * <b>This package defines Java object classes for our representations of calendar events.
 * <ul>
 * <li> CalendarObject: Defines the base class for what we expect an 'event' to hold, that is: A date, a title, and a description.
 * <li> Event: Defines a class that inherits from CalendarObject, but also contains a time.
 * <li> Meeting: Defines a class that inherits from CalendarObject, but also contains a location.
 * </ul>
 * @author Jakub Zywicki, Marina Ljuboja, Benito Ohara
 */